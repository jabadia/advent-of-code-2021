def solve(input):
    unique_digits = 0
    for line in input.strip().split('\n'):
        pattern, output = line.split(' | ')
        digits = output.split(' ')
        unique_digits += sum(len(digit) in {2, 4, 3, 7} for digit in digits)
    return unique_digits


